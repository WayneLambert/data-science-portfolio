# In-place value swapping is easily achieved in
# Python using the following shorthand...

# Assign original values to a and b
a, b = 1, 2

# Diaply them
print(f"{'Original:'} {a}{','} {b}")            # Prints Original: 1, 2

# Swap the values
a, b = b, a

# Show the swap
print(f"{'Swapped:'} {a}{','} {b}")             # Prints Swapped: 2, 1
